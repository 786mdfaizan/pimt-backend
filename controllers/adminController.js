// PATCH /api/admin/students/:id
exports.updateStudent = async (req, res) => {
  try {
    const allowed = ['rollNumber', 'course', 'batch', 'semester', 'bloodGroup'];
    const updates = {};
    allowed.forEach(f => { if (req.body[f] !== undefined) updates[f] = req.body[f]; });
    const student = await Student.findByIdAndUpdate(req.params.id, updates, { new: true });
    res.json({ message: 'Student updated', student });
  } catch (err) {
    if (err.code === 11000) return res.status(400).json({ message: 'Roll number already in use' });
    res.status(500).json({ message: err.message });
  }
};

// DELETE /api/admin/students/:id
exports.deleteStudent = async (req, res) => {
  try {
    const student = await Student.findByIdAndUpdate(req.params.id, { isActive: false });
    if (!student) return res.status(404).json({ message: 'Student not found' });
    await User.findByIdAndUpdate(student.user, { isActive: false });
    res.json({ message: 'Student deactivated' });
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

// POST /api/admin/sub-admins  — supports all 4 new roles
exports.createSubAdmin = async (req, res) => {
  try {
    const { name, email, password, role, phone, basicSalary } = req.body;

    if (!ALLOWED_STAFF_ROLES.includes(role))
      return res.status(400).json({ message: `Role must be one of: ${ALLOWED_STAFF_ROLES.join(', ')}` });

    const existing = await User.findOne({ email });
    if (existing) return res.status(400).json({ message: 'Email already in use' });

    const user = await User.create({
      name, email, password, role, phone,
      basicSalary: basicSalary || 0,
      createdBy: req.user._id,
      // deviceUserId auto-generated by User model pre-save hook
    });

    res.status(201).json({
      message: 'Staff created',
      user: {
        id:           user._id,
        name:         user.name,
        email:        user.email,
        role:         user.role,
        deviceUserId: user.deviceUserId,
        basicSalary:  user.basicSalary,
      },
    });
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

