const User    = require('../models/User');
const Student = require('../models/Student');
const Group   = require('../models/Group');

const ALLOWED_STAFF_ROLES = ['faculty', 'counselor', 'marketing', 'office_staff'];

// GET /api/admin/dashboard
exports.getDashboardStats = async (req, res) => {
  try {
    const [totalStudents, totalFaculty, totalCounselors, totalMarketing, totalOfficeStaff] = await Promise.all([
      Student.countDocuments({ isActive: true }),
      User.countDocuments({ role: 'faculty',       isActive: true }),
      User.countDocuments({ role: 'counselor',     isActive: true }),
      User.countDocuments({ role: 'marketing',     isActive: true }),
      User.countDocuments({ role: 'office_staff',  isActive: true }),
    ]);
    const Fee       = require('../models/Fee');
    const { Leave } = require('../models/index');
    const [totalFeeCollected, pendingLeaves] = await Promise.all([
      Fee.aggregate([{ $match: { status: 'paid' } }, { $group: { _id: null, total: { $sum: '$amount' } } }]),
      Leave.countDocuments({ status: 'pending' }),
    ]);
    res.json({
      totalStudents,
      totalFaculty,
      totalCounselors,
      totalMarketing,
      totalOfficeStaff,
      totalStaff: totalFaculty + totalCounselors + totalMarketing + totalOfficeStaff,
      totalFeeCollected: totalFeeCollected[0]?.total || 0,
      pendingLeaves,
    });
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

// POST /api/admin/students
exports.createStudent = async (req, res) => {
  try {
    const {
      name, email, password, phone,
      course, batch, semester, dob,
      address, guardianName, guardianPhone, bloodGroup,
    } = req.body;

    const existing = await User.findOne({ email });
    if (existing) return res.status(400).json({ message: 'Email already registered' });

    const user = await User.create({
      name, email,
      password: password || 'PIMT@1234',
      role: 'student',
      phone,
      createdBy: req.user._id,
    });

    const student = await Student.create({
      user: user._id,
      course, batch,
      semester: semester || 1,
      dob, address,
      guardianName, guardianPhone, bloodGroup,
    });

    res.status(201).json({
      message:      'Student created successfully',
      deviceUserId: user.deviceUserId,
      rollNumber:   student.rollNumber,
      user: {
        id:           user._id,
        name:         user.name,
        email:        user.email,
        deviceUserId: user.deviceUserId,
      },
      student: { id: student._id, rollNumber: student.rollNumber },
    });
  } catch (err) {
    res.status(500).json({ message: err.message });
  }
};

